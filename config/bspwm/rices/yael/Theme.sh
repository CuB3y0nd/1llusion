set_jgmenu_colors() {
  sed -i "$HOME"/.config/bspwm/jgmenurc \
    -e 's/color_menu_bg = .*/color_menu_bg = #161616/' \
    -e 's/color_norm_fg = .*/color_norm_fg = #ffffff/' \
    -e 's/color_sel_bg = .*/color_sel_bg = #212121/' \
    -e 's/color_sel_fg = .*/color_sel_fg = #ffffff/' \
    -e 's/color_sep_fg = .*/color_sep_fg = #33b1ff/'
}

set_launcher_config() {
  cat >"$HOME"/.config/bspwm/src/rofi-themes/shared.rasi <<EOF
// Rofi colors for Yael

* {
    font: "JetBrainsMono NF Bold 9";
    background: #161616;
    background-alt: #161616E0;
    foreground: #ffffff;
    selected: #33b1ff;
    active: #42be65;
    urgent: #ee5396;

    img-background: url("~/.config/bspwm/rices/yael/rofi.webp", width);
}
EOF
}

launch_bars() {
  for mon in $(polybar --list-monitors | cut -d":" -f1); do
    MONITOR=$mon polybar -q yael-cohen -c "${rice_dir}"/config.ini &
  done
}

set_bspwm_config
set_alacritty_config
set_kitty_config
set_picom_config
set_eww_colors
set_jgmenu_colors
set_dunst_config
set_launcher_config
launch_bars
