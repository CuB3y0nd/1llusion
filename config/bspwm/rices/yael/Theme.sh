#!/usr/bin/env bash

set_bspwm_config() {
  bspc config border_width 0
  bspc config top_padding 38
  bspc config bottom_padding 2
  bspc config left_padding 2
  bspc config right_padding 2
  bspc config normal_border_color "#33b1ff"
  bspc config active_border_color "#42be65"
  bspc config focused_border_color "#33b1ff"
  bspc config presel_feedback_color "#ee5396"
}

